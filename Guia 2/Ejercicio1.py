#Pido al usuario el ingreso de dos numeros para luego sumarlos
num1=int(input("Ingrese un primer numero: "))
num2=int(input("Ingrese un segundo numero: "))
#formula para la suma
suma=num1+num2
#Formula para la diferencia
diferencia=num1-num2
#Formula para el producto
producto=num1*num2
#Salidas por pantalla
print(f"La suma de los dos numero es: {suma} ")
print(f"La diferencia entre los dos numeros es de: {diferencia} ")
print(f"El producto es: {num1} x {num2} = {producto}")
